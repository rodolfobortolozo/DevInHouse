CREATE TABLE PROFESSOR(
  IDPROFESSOR NUMBER(19),
  NOME VARCHAR2(200),
  SOBRENOME VARCHAR2(200),
  TELEFONE VARCHAR2(15),
  EMAIL VARCHAR2(200),
  DTANASCIMENTO DATE, 
  CIDADE VARCHAR(200),
  NACIONALIDADE VARCHAR2(200),
  
  PRIMARY KEY (IDPROFESSOR) 
)
/
CREATE TABLE ESTUDANTE(
  IDESTUDANTE NUMBER(19),
  NOME VARCHAR2(200),
  SOBRENOME VARCHAR2(200),
  MATRICULA NUMBER(19),
  TELEFONE VARCHAR2(15),
  EMAIL VARCHAR(200),
  DTANASCIMENTO DATE, 
  NACIONALIDADE VARCHAR2(200),
  MEDIAGERAL NUMBER(19,3),
  
  PRIMARY KEY (IDESTUDANTE)
)
/
CREATE TABLE CURSO(
  IDCURSO NUMBER(19),
  NOME VARCHAR2(200),
  EAD CHAR(1),
  IDPROFESSOR NUMBER(19),
  NROMAXALUNO NUMBER(19),
  
  PRIMARY KEY (IDCURSO),
  FOREIGN KEY (IDPROFESSOR) REFERENCES PROFESSOR (IDPROFESSOR)
)

/
CREATE TABLE MATRICULA(
  IDMATRICULA NUMBER(19),
  IDESTUDANTE NUMBER(19),
  IDCURSO NUMBER(19),
  MEDIA NUMBER(19,3),
  
  PRIMARY KEY (IDMATRICULA),
  FOREIGN KEY (IDESTUDANTE) REFERENCES ESTUDANTE (IDESTUDANTE),
  FOREIGN KEY (IDCURSO) REFERENCES CURSO (IDCURSO)
)
/
